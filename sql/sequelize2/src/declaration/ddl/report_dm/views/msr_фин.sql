CREATE OR REPLACE VIEW report_dm.msr_фин AS
SELECT опл_ид,
    договор_ид,
    вид_реал_ид,
    дата,
    NULL AS док_нач_ид,
    NULL AS погаш_оплатой_без_аванса,
    NULL AS погаш_оплатой_аванс,
    NULL AS погаш_из_перепл,
    NULL AS погаш_из_аванса,
    NULL AS погаш_оплатой,
    NULL AS погаш_из_кред,
    NULL AS погаш,
    NULL AS погаш_без_аванса,
    опл_кред_перепл,
    опл_кред_аванс,
    сторно_кред_перепл,
    обор_кред,
    опл_кред,
    сторно_кред
FROM report_dm.msr_фин_опл_кредит
UNION ALL
SELECT опл_ид,
    договор_ид,
    вид_реал_ид,
    дата,
    док_нач_ид,
    погаш_оплатой_без_аванса,
    погаш_оплатой_аванс,
    погаш_из_перепл,
    погаш_из_аванса,
    погаш_оплатой,
    погаш_из_кред,
    погаш,
    погаш_без_аванса,
    NULL AS опл_кред_перепл,
    NULL AS опл_кред_аванс,
    NULL AS сторно_кред_перепл,
    NULL AS обор_кред,
    NULL AS опл_кред,
    NULL AS сторно_кред
FROM report_dm.msr_фин_опл_погаш;